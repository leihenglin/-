package com.example.testjpa.po;

import javax.persistence.*;

@Entity
@Table(name = "userjpa")
public class User {
    @Id
    @GeneratedValue(strategy= GenerationType.IDENTITY)
    public int id;
    public String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}